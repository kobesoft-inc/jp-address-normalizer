<?php

declare(strict_types=1);

namespace JpAddressNormalizer\Internal;

/**
 * 一般的なロジックでは判定できない、市区町村（city_code）ごとの既知の例外パターンを
 * 保持するテーブル。houjin-bangou-db（法人番号データベース）の実データを検証して
 * 見つかった、実際に判定可能なケースのみを収録している（推測が必要なケースは含めない）。
 *
 * ## 字名+建物名の判別（`AZA_NAMES`）
 *
 * 「小笠原村父島字小曲コーポパッション103」のように、字名と建物名の間に数字も
 * 区切り文字も無いケースは、実在する字名を知らなければ機械的に分離できない
 * （`StreetBuildingSplitter`の一般ロジックは、番地の開始位置を探すのに数字や
 * 「丁目」「番地」等のキーワードを手がかりにするため、区切りが無いと
 * 「字小曲コーポパッション」を丸ごと字名として扱ってしまう）。
 *
 * ここに実在する字名を登録しておくことで、その字名の直後で正しく区切れるようになる。
 *
 * ## 旧区名等の接頭辞の省略（`OMITTABLE_TOWN_PREFIXES`）
 *
 * 「東京都千代田区三崎町」のように、DB上の正式な町名は「神田三崎町」（かつての
 * 神田区に由来する接頭辞付き）だが、実際の住所では「神田」を省いて「三崎町」とだけ
 * 書かれることが非常に多い。東京都中央区の「日本橋」（日本橋大伝馬町→大伝馬町 等）も
 * 同様の合併前の区名に由来する接頭辞で、houjin-bangou-db全件の検証で特に件数が多く、
 * 明確に判定可能なパターンだったため収録している。
 *
 * 「○○町○○」の大字名繰り返し省略（`TownMatcher`側で実装）と同じ安全策として、
 * 接頭辞を除いた名前が同じ市区町村内の別の独立した町名と衝突する場合は追加しない。
 *
 * ## 括弧書きの旧地名の分割（`looksLikeObsoleteAnnotation`）
 *
 * 「東京都文京区（東京市小石川区久堅町91番地）」のように、法人登記の住所には戦前・戦後の
 * 市区再編前の旧地名が括弧書きで残っていることがある（東京23区の前身35区の統合、大阪市・
 * 名古屋市・横浜市・札幌市・神戸市等での区の分割・統合など）。これらの旧地名は現在の
 * 参照データに存在しないため町名としてマッチせず、郵便番号を判定することはできない
 * （できたとしても、現在の郵便番号制度とは無関係な情報であり意味が無い）。
 *
 * 旧地名そのものは全国の区再編の数だけ存在し、個別に列挙するのは非現実的かつ際限が無い。
 * 一方、これらは共通して「町名の直後に丸括弧が来る」という書式上の強い特徴を持つ
 * （現在実在する町名が丸括弧から始まることは無い）。そのため、個別の旧地名を列挙する
 * のではなく、この書式上の特徴だけで「旧地名の注記である」ことを判定し、`StreetBuildingSplitter`
 * が番地の開始位置を手がかりに、旧地名部分・番地部分・建物名部分へ機械的に分割できるようにする。
 * 郵便番号や町名としての意味付けはせず、あくまで文字列としての分割（`format()`で元の表記に
 * 復元できること）のみを目的とする。
 *
 * ## 京都の通り名が無いケースを含めない理由
 *
 * 「京都市中京区河原町通御池下る大黒町5番地」のように、通り名（例:「河原町通御池下る」）が
 * 省略され、かつ同じ町名が複数の郵便番号ブロックに分かれている場合、その番地がどのブロックに
 * 属するかを住所文字列だけから決める情報は無い。この場合、テーブルにどちらか一方の郵便番号を
 * 決め打ちで登録することは可能だが、それは「判定」ではなく「推測」であり、外れれば誤った
 * 郵便番号を返すことになる。本ライブラリは一貫して「わからないものは`unresolvedReason`を
 * 返し、推測はしない」という方針のため、意図的にここには含めていない。
 */
final class AddressExceptions
{
    /**
     * city_code => 既知の字名の一覧（長い順）。
     * `StreetBuildingSplitter`が「大字」「字」に続く字名の終わりを、
     * 番地や建物名の開始位置から逆算するのではなく、ここに登録された
     * 実在の字名と直接照合することで判定する。
     *
     * @var array<string, list<string>>
     */
    private const AZA_NAMES = [
        // 東京都小笠原村: 「字小曲コーポパッション103」→ 字名「小曲」+ 建物名「コーポパッション103」
        '13421' => ['小曲'],
        // 岩手県大船渡市: 「字みどり町コーポ田中嶋101」→ 字名「みどり町」+ 建物名「コーポ田中嶋101」
        '03203' => ['みどり町'],
    ];

    /**
     * city_code => 省略されうる旧区名等の町名接頭辞の一覧。
     *
     * @var array<string, list<string>>
     */
    private const OMITTABLE_TOWN_PREFIXES = [
        // 東京都千代田区: 神田三崎町 → 三崎町 等（旧神田区に由来する28町全てに付く接頭辞）
        '13101' => ['神田'],
        // 東京都中央区: 日本橋大伝馬町 → 大伝馬町 等（旧日本橋区に由来する接頭辞）
        '13102' => ['日本橋'],
        // 東京都新宿区: 四谷本塩町 → 本塩町 等（住居表示未実施地区で使われる旧四谷区由来の接頭辞）
        '13104' => ['四谷'],
    ];

    /**
     * $textの先頭（「大字」「字」を除いた部分）が、$cityCodeの既知の字名と一致するか調べる。
     * 一致すればその字名を返す（「大字」「字」自体は含まない）。
     */
    public static function matchKnownAza(string $cityCode, string $text): ?string
    {
        foreach (self::AZA_NAMES[$cityCode] ?? [] as $name) {
            if (str_starts_with($text, $name)) {
                return $name;
            }
        }
        return null;
    }

    /** @return list<string> */
    public static function omittableTownPrefixes(string $cityCode): array
    {
        return self::OMITTABLE_TOWN_PREFIXES[$cityCode] ?? [];
    }

    /**
     * $textが、括弧書きの旧地名の注記らしい形（丸括弧で始まる）かどうかを判定する。
     * 現在実在する町名が丸括弧から始まることは無いため、市区町村を問わず適用できる。
     */
    public static function looksLikeObsoleteAnnotation(string $text): bool
    {
        return str_starts_with($text, '（') || str_starts_with($text, '(');
    }
}
